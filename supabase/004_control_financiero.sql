-- ───────────────────────────────────────────────────────────────
-- 2. FUNCIÓN nextval (wrapper para secuencias desde el frontend)
-- ───────────────────────────────────────────────────────────────
CREATE OR REPLACE FUNCTION nextval(seq_name TEXT)
RETURNS BIGINT LANGUAGE plpgsql AS $$
DECLARE result BIGINT;
BEGIN
  EXECUTE format('SELECT nextval(%L)', seq_name) INTO result;
  RETURN result;
END;
$$;

-- ───────────────────────────────────────────────────────────────
-- 3. CREAR TABLA cuentas_por_cobrar
-- ───────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS cuentas_por_cobrar (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  cliente_id      BIGINT NOT NULL REFERENCES clientes(id),
  orden_id        BIGINT REFERENCES ordenes(id),
  fecha_venta     DATE NOT NULL DEFAULT CURRENT_DATE,
  fecha_vencimiento DATE,
  monto_original  NUMERIC NOT NULL DEFAULT 0,
  monto_pagado    NUMERIC NOT NULL DEFAULT 0,
  saldo_pendiente NUMERIC NOT NULL DEFAULT 0,
  estatus         TEXT NOT NULL DEFAULT 'Pendiente',  -- Pendiente, Parcial, Pagada, Vencida
  concepto        TEXT DEFAULT '',
  created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at      TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- RLS para cuentas_por_cobrar
ALTER TABLE cuentas_por_cobrar ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "cxc_read_all" ON cuentas_por_cobrar;
DROP POLICY IF EXISTS "cxc_write" ON cuentas_por_cobrar;
CREATE POLICY "cxc_read_all" ON cuentas_por_cobrar FOR SELECT TO authenticated USING (true);
CREATE POLICY "cxc_write" ON cuentas_por_cobrar FOR ALL TO authenticated USING (true) WITH CHECK (true);

