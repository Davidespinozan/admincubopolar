-- ───────────────────────────────────────────────────────────────
-- 7. FUNCIÓN para incrementar/decrementar saldo de cliente
-- ───────────────────────────────────────────────────────────────
CREATE OR REPLACE FUNCTION increment_saldo(p_cli BIGINT, p_delta NUMERIC)
RETURNS void LANGUAGE plpgsql AS $$
BEGIN
  UPDATE clientes SET saldo = saldo + p_delta WHERE id = p_cli;
END;
$$;

-- ───────────────────────────────────────────────────────────────
-- 8. TRIGGERS updated_at (si no existen)
-- ───────────────────────────────────────────────────────────────
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN NEW.updated_at = now(); RETURN NEW; END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_cxc_updated ON cuentas_por_cobrar;
CREATE TRIGGER trg_cxc_updated BEFORE UPDATE ON cuentas_por_cobrar 
FOR EACH ROW EXECUTE FUNCTION update_updated_at();
